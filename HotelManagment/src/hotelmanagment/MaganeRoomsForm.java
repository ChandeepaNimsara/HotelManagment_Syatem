    private void jButtonAddRoomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddRoomActionPerformed

        String num = jTextFieldRoomNumber.getText();
        String typ = jComboBoxTypeRoom.getSelectedItem().toString();
        String tel = jTextFieldPhoneNoRoom.getText();

        try{
            
            String query = "INSERT INTO `rooms`(`r_number`, `type`, `phone`, `reserved`) VALUES (?,?,?,?)";
            con = DriverManager.getConnection("jdbc:mysql://localhost/java_hotel_db", "root", "");
            st = con.prepareStatement(query);
    
            st.setString(1, jTextFieldRoomNumber.getText());
            st.setString(2, jComboBoxTypeRoom.getSelectedItem().toString());
            st.setString(3, jTextFieldPhoneNoRoom.getText());
            st.setString(4, "No");
 
            if(num.trim().equals("") || typ.trim().equals("") || tel.trim().equals(""))
            {
                JOptionPane.showMessageDialog(rootPane, "Required Fields: Id, First Name, Last Name, Mobile No", "Check Data", JOptionPane.INFORMATION_MESSAGE);
            }else{
            
            st.executeUpdate();
            JOptionPane.showMessageDialog(null, "Room Add Successfully...");
            }
            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        } 
    }//GEN-LAST:event_jButtonAddRoomActionPerformed

    private void jButtonUpdateRoomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateRoomActionPerformed

          String num = jTextFieldRoomNumber.getText();
          String typ = jComboBoxTypeRoom.getSelectedItem().toString();
          String tel = jTextFieldPhoneNoRoom.getText();
          String res = "No";
          
          if(jRadioButtonYes.isSelected()){
              res = "Yes";
          }
        
          try{
                String UpdateQuery = "UPDATE `rooms` SET `r_number`='"+num+"',`type`='"+typ+"',`phone`='"+tel+"',`reserved`='"+res+"'WHERE r_number="+num+"";
                con = DriverManager.getConnection("jdbc:mysql://localhost/java_hotel_db", "root", "");
                st = con.prepareStatement(UpdateQuery);
                st.execute();
                JOptionPane.showMessageDialog(null, "Room has Successfully Updated...");
            
           }catch(Exception e){
               JOptionPane.showMessageDialog(rootPane, e);
           }  
    }//GEN-LAST:event_jButtonUpdateRoomActionPerformed

    private void jButtonRemoveRoomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemoveRoomActionPerformed

        //remove selected rooms
        try{
            int roomNumber = Integer.valueOf(jTextFieldRoomNumber.getText());
            
             if(rooms.removeRoom(roomNumber)){
                
                JOptionPane.showMessageDialog(rootPane, "Room Data deleted Successfully...", "Remove Room",JOptionPane.INFORMATION_MESSAGE);
                
            }else{
                JOptionPane.showMessageDialog(rootPane, "Room Data Not Deletd", "Remove Room Erroe", JOptionPane.ERROR_MESSAGE);
            }
        }
        catch(NumberFormatException ex){
                JOptionPane.showMessageDialog(rootPane, ex.getMessage() + " - Enter The Room Number","Room Number Error", JOptionPane.ERROR_MESSAGE );
                }    
    }//GEN-LAST:event_jButtonRemoveRoomActionPerformed

