    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked

        DefaultTableModel model = (DefaultTableModel)jTable2.getModel();
        
        int rIndex = jTable2.getSelectedRow();
        
        jTextFieldRoomNumber.setText(model.getValueAt(rIndex, 0).toString());
        jComboBoxTypeRoom.setSelectedItem(model.getValueAt(rIndex, 1));
        jTextFieldPhoneNoRoom.setText(model.getValueAt(rIndex, 2).toString());
        
        String isReeserved = model.getValueAt(rIndex, 3).toString();
        
        if(isReeserved.equals("YES")){
            jRadioButtonYes.setSelected(true);
        }
        else if(isReeserved.equals("NO")){
            jRadioButtonNo.setSelected(true);
        }
    }//GEN-LAST:event_jTable2MouseClicked

